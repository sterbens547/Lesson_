public class Comparison {

    public static String comp (int a,int b) {
        if (a > b) {
            System.out.println(a + " больше " + b);
        }
        else if (a < b ) {
            System.out.println(a + " меньше " + b);
        }
        else {
            System.out.println(a + " равно " + b);
        }
        return "";
    }
}
